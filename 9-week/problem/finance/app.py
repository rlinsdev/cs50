    """Get stock quote."""

    if request.method == "POST":

        symbol = request.form.get("symbol")
        if not symbol:
            return apology("Symbol required", 400)
        quoted = lookup(symbol)
        return render_template("quote.html", quoted=quoted)

    return render_template("quote.html")


